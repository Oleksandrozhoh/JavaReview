package week03;

public class LogicalOperators {
    public static void main(String[] args) {

        // Logical operators: && , || , !
        // &&: both conditions should be true for output should be true
        System.out.println(true && false); // going to be false
        // ||: one of the conditions should be true for output to be true
        System.out.println(true || false); // going to be true
        // !: not , opposite




    }
}
